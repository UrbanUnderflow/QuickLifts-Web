const fs = require('fs');
const path = require('path');
const readline = require('readline');
const { chromium } = require('@playwright/test');
const { GoogleAuth, JWT } = require('google-auth-library');

const baseURL = process.env.PLAYWRIGHT_BASE_URL || 'http://localhost:3000';
const outputPath = process.env.PLAYWRIGHT_STORAGE_STATE
  ? path.resolve(process.env.PLAYWRIGHT_STORAGE_STATE)
  : path.resolve(process.cwd(), '.playwright/admin-storage-state.json');
const remoteLoginToken = process.env.PLAYWRIGHT_REMOTE_LOGIN_TOKEN;
const bootstrapSecretName = process.env.PLAYWRIGHT_BOOTSTRAP_SECRET_NAME || '';
const bootstrapJson = process.env.PLAYWRIGHT_BOOTSTRAP_JSON || '';
const bootstrapEnvPath = path.resolve(process.cwd(), '.playwright/bootstrap.env');
const bootstrapDefaultNextPath = '/admin/systemOverview#variant-registry';
const secretManagerScope = 'https://www.googleapis.com/auth/cloud-platform';

function parseEnvValue(rawValue) {
  let value = rawValue.trim();

  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    value = value.slice(1, -1);
  }

  return value.replace(/\\n/g, '\n');
}

function loadEnvFile(filePath) {
  if (!fs.existsSync(filePath)) {
    return;
  }

  const content = fs.readFileSync(filePath, 'utf8');
  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#')) continue;

    const normalized = line.startsWith('export ') ? line.slice(7).trim() : line;
    const separatorIndex = normalized.indexOf('=');
    if (separatorIndex <= 0) continue;

    const key = normalized.slice(0, separatorIndex).trim();
    const value = parseEnvValue(normalized.slice(separatorIndex + 1));
    if (!(key in process.env)) {
      process.env[key] = value;
    }
  }
}

loadEnvFile(path.resolve(process.cwd(), '.env.local'));
loadEnvFile(bootstrapEnvPath);

function ensureDir(filePath) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
}

function escapeShellValue(value) {
  return String(value).replace(/'/g, `'\\''`);
}

function writeBootstrapEnvFile(envConfig) {
  if (!envConfig) return;

  const lines = [
    '# Generated by npm run test:e2e:auth',
    `export PLAYWRIGHT_STORAGE_STATE='${escapeShellValue(outputPath)}'`,
  ];

  if (envConfig.organizationId) {
    lines.push(`export PLAYWRIGHT_PULSECHECK_ORG_ID='${escapeShellValue(envConfig.organizationId)}'`);
  }
  if (envConfig.teamId) {
    lines.push(`export PLAYWRIGHT_PULSECHECK_TEAM_ID='${escapeShellValue(envConfig.teamId)}'`);
  }
  if (envConfig.namespace) {
    lines.push(`export PLAYWRIGHT_E2E_NAMESPACE='${escapeShellValue(envConfig.namespace)}'`);
  }
  if (envConfig.baseURL) {
    lines.push(`export PLAYWRIGHT_BASE_URL='${escapeShellValue(envConfig.baseURL)}'`);
  }

  ensureDir(bootstrapEnvPath);
  fs.writeFileSync(bootstrapEnvPath, `${lines.join('\n')}\n`, 'utf8');
}

function waitForEnter(promptText) {
  return new Promise((resolve) => {
    const rl = readline.createInterface({
      input: process.stdin,
      output: process.stdout,
    });
    rl.question(promptText, () => {
      rl.close();
      resolve();
    });
  });
}

function normalizePrivateKey(value) {
  if (!value) return null;

  let normalized = String(value).trim();
  if (
    (normalized.startsWith('"') && normalized.endsWith('"')) ||
    (normalized.startsWith("'") && normalized.endsWith("'"))
  ) {
    normalized = normalized.slice(1, -1);
  }

  normalized = normalized.replace(/\\\\n/g, '\n').replace(/\\n/g, '\n');
  return normalized || null;
}

function parseSerializedServiceAccount(raw) {
  if (!raw) return null;

  try {
    const parsed = JSON.parse(raw);
    return {
      projectId: parsed.project_id || parsed.projectId || null,
      clientEmail: parsed.client_email || parsed.clientEmail || null,
      privateKey: normalizePrivateKey(parsed.private_key || parsed.privateKey || null),
    };
  } catch (_error) {
    return null;
  }
}

function getInlineFirebasePrivateKey() {
  const direct = normalizePrivateKey(process.env.FIREBASE_PRIVATE_KEY || process.env.FIREBASE_SECRET_KEY);
  if (direct) return direct;

  if (process.env.FIREBASE_PRIVATE_KEY_1) {
    return normalizePrivateKey(
      [
        process.env.FIREBASE_PRIVATE_KEY_1 || '',
        process.env.FIREBASE_PRIVATE_KEY_2 || '',
        process.env.FIREBASE_PRIVATE_KEY_3 || '',
        process.env.FIREBASE_PRIVATE_KEY_4 || '',
      ].join('')
    );
  }

  return null;
}

function getRuntimeServiceAccountCredential() {
  const explicit =
    parseSerializedServiceAccount(process.env.GCP_SECRET_MANAGER_SERVICE_ACCOUNT_JSON) ||
    parseSerializedServiceAccount(process.env.GCP_SERVICE_ACCOUNT_JSON) ||
    parseSerializedServiceAccount(process.env.FIREBASE_SERVICE_ACCOUNT);

  if (explicit?.clientEmail && explicit.privateKey) {
    return explicit;
  }

  const clientEmail = process.env.FIREBASE_CLIENT_EMAIL || null;
  const privateKey = getInlineFirebasePrivateKey();
  const projectId =
    process.env.GOOGLE_SECRET_MANAGER_PROJECT_ID ||
    process.env.GOOGLE_CLOUD_PROJECT ||
    process.env.GCP_PROJECT ||
    process.env.FIREBASE_PROJECT_ID ||
    process.env.DEV_FIREBASE_PROJECT_ID ||
    null;

  if (!clientEmail || !privateKey) {
    return null;
  }

  return {
    projectId,
    clientEmail,
    privateKey,
  };
}

async function getSecretManagerAuth() {
  const explicitCredential = getRuntimeServiceAccountCredential();

  if (explicitCredential?.clientEmail && explicitCredential.privateKey) {
    const client = new JWT({
      email: explicitCredential.clientEmail,
      key: explicitCredential.privateKey,
      scopes: [secretManagerScope],
    });

    const accessTokenResult = await client.getAccessToken();
    const accessToken =
      typeof accessTokenResult === 'string' ? accessTokenResult : accessTokenResult?.token;

    if (!accessToken) {
      throw new Error('Failed to retrieve Secret Manager access token from explicit service-account credentials.');
    }

    return {
      accessToken,
      projectId:
        process.env.GOOGLE_SECRET_MANAGER_PROJECT_ID ||
        process.env.GOOGLE_CLOUD_PROJECT ||
        process.env.GCP_PROJECT ||
        explicitCredential.projectId,
    };
  }

  const auth = new GoogleAuth({ scopes: [secretManagerScope] });
  const client = await auth.getClient();
  const accessTokenResult = await client.getAccessToken();
  const accessToken =
    typeof accessTokenResult === 'string'
      ? accessTokenResult
      : accessTokenResult?.token || accessTokenResult?.res?.data?.access_token;
  const projectId =
    process.env.GOOGLE_SECRET_MANAGER_PROJECT_ID ||
    process.env.GOOGLE_CLOUD_PROJECT ||
    process.env.GCP_PROJECT ||
    await auth.getProjectId().catch(() => null);

  if (!accessToken) {
    throw new Error('Failed to retrieve Secret Manager access token from application default credentials.');
  }

  return { accessToken, projectId };
}

async function getSecretManagerSecret(secretName) {
  const normalizedName = String(secretName || '').trim();
  if (!normalizedName) {
    throw new Error('Secret name is required.');
  }

  const { accessToken, projectId } = await getSecretManagerAuth();
  if (!projectId) {
    throw new Error('Missing Google Cloud project id for Secret Manager access.');
  }

  const response = await fetch(
    `https://secretmanager.googleapis.com/v1/projects/${encodeURIComponent(projectId)}/secrets/${encodeURIComponent(normalizedName)}/versions/latest:access`,
    {
      method: 'GET',
      headers: {
        Authorization: `Bearer ${accessToken}`,
        Accept: 'application/json',
      },
    }
  );

  const payload = await response.json().catch(() => ({}));
  if (!response.ok) {
    throw new Error(payload?.error?.message || `Failed to access Secret Manager secret ${normalizedName}.`);
  }

  const encoded = payload?.payload?.data;
  if (!encoded) {
    throw new Error(`Secret Manager secret ${normalizedName} returned an empty payload.`);
  }

  return Buffer.from(encoded, 'base64').toString('utf8');
}

function parseBootstrapConfig(raw) {
  if (!raw) return null;

  let parsed;
  try {
    parsed = JSON.parse(raw);
  } catch (error) {
    throw new Error(`Failed to parse Playwright bootstrap JSON: ${error.message}`);
  }

  const adminEmail = (parsed.adminEmail || parsed.email || '').trim().toLowerCase();
  const adminUid = (parsed.adminUid || parsed.uid || '').trim();
  const nextPath = (parsed.nextPath || parsed.path || bootstrapDefaultNextPath).trim() || bootstrapDefaultNextPath;

  if (!adminEmail && !adminUid) {
    throw new Error('Playwright bootstrap config requires `adminEmail` or `adminUid`.');
  }

  return {
    adminEmail: adminEmail || null,
    adminUid: adminUid || null,
    nextPath,
    organizationId: (parsed.pulseCheckOrganizationId || parsed.organizationId || '').trim() || null,
    teamId: (parsed.pulseCheckTeamId || parsed.teamId || '').trim() || null,
    namespace: (parsed.playwrightNamespace || parsed.namespace || '').trim() || null,
    baseURL: (parsed.baseURL || parsed.playwrightBaseURL || '').trim() || null,
    label: (parsed.label || parsed.name || '').trim() || null,
  };
}

async function loadBootstrapConfig() {
  if (bootstrapJson) {
    return {
      source: 'PLAYWRIGHT_BOOTSTRAP_JSON',
      config: parseBootstrapConfig(bootstrapJson),
    };
  }

  if (bootstrapSecretName) {
    const secretValue = await getSecretManagerSecret(bootstrapSecretName);
    return {
      source: `Secret Manager (${bootstrapSecretName})`,
      config: parseBootstrapConfig(secretValue),
    };
  }

  return null;
}

async function buildBootstrapCustomToken(bootstrapConfig) {
  const { getFirebaseAdminApp } = require('../netlify/functions/config/firebase');
  const request = {
    headers: {
      origin: baseURL,
      referer: baseURL,
      'x-force-dev-firebase': 'true',
    },
  };
  const adminApp = getFirebaseAdminApp(request);
  const adminAuth = adminApp.auth();
  const userRecord = bootstrapConfig.adminUid
    ? await adminAuth.getUser(bootstrapConfig.adminUid)
    : await adminAuth.getUserByEmail(bootstrapConfig.adminEmail);

  const customToken = await adminAuth.createCustomToken(userRecord.uid, {
    playwrightBootstrap: true,
    bootstrapSource: bootstrapConfig.label || 'secret-manager-bootstrap',
  });

  return {
    customToken,
    user: {
      uid: userRecord.uid,
      email: userRecord.email || bootstrapConfig.adminEmail || '',
    },
  };
}

async function main() {
  ensureDir(outputPath);

  const bootstrap = await loadBootstrapConfig().catch((error) => {
    throw new Error(`Failed to load Playwright bootstrap config: ${error.message}`);
  });

  const browser = await chromium.launch({
    headless: false,
    slowMo: 50,
  });

  const context = await browser.newContext();
  await context.addInitScript(() => {
    window.localStorage.setItem('forceDevFirebase', 'true');
  });
  const page = await context.newPage();

  try {
    if (bootstrap?.config) {
      const { customToken, user } = await buildBootstrapCustomToken(bootstrap.config);
      const bootstrapBaseURL = bootstrap.config.baseURL || baseURL;
      const nextPath = bootstrap.config.nextPath || bootstrapDefaultNextPath;

      await page.goto(
        `${bootstrapBaseURL}/remote-login?token=${encodeURIComponent(customToken)}&userId=${encodeURIComponent(user.uid)}&email=${encodeURIComponent(user.email)}&next=${encodeURIComponent(nextPath)}`,
        {
          waitUntil: 'networkidle',
        }
      );
      await page.waitForTimeout(2000);
      writeBootstrapEnvFile({
        organizationId: bootstrap.config.organizationId,
        teamId: bootstrap.config.teamId,
        namespace: bootstrap.config.namespace,
        baseURL: bootstrap.config.baseURL || null,
      });
      console.log('');
      console.log(`Playwright auth bootstrap loaded from ${bootstrap.source}.`);
      console.log(`Saved helper env exports to: ${bootstrapEnvPath}`);
      console.log(`Source them with: source ${bootstrapEnvPath}`);
    } else if (remoteLoginToken) {
      const next = bootstrapDefaultNextPath;
      await page.goto(`${baseURL}/remote-login?token=${encodeURIComponent(remoteLoginToken)}&next=${encodeURIComponent(next)}`, {
        waitUntil: 'networkidle',
      });
      await page.waitForTimeout(2000);
    } else {
      await page.goto(`${baseURL}${bootstrapDefaultNextPath}`, {
        waitUntil: 'domcontentloaded',
      });
      console.log('');
      console.log('Playwright auth capture is open in a browser window.');
      console.log('1. Complete admin login in the browser.');
      console.log('2. Make sure you can see the Variant Registry or admin area.');
      console.log('3. Return here and press Enter to save storage state.');
      console.log('');
      await waitForEnter('Press Enter after admin login is complete...');
    }

    await context.storageState({ path: outputPath, indexedDB: true });
    console.log('');
    console.log(`Saved Playwright admin storage state to: ${outputPath}`);
    console.log('You can now run:');
    console.log('npm run test:e2e');
    if (fs.existsSync(bootstrapEnvPath)) {
      console.log('');
      console.log(`Optional helper exports are ready at: ${bootstrapEnvPath}`);
    }
    console.log('');
  } finally {
    await browser.close();
  }
}

main().catch((error) => {
  console.error('Failed to capture Playwright admin storage state:', error);
  process.exit(1);
});
